public class Operadores62 {
    public static void main(String[] args) {
        int numero1 = 1;
        int numero2 = 2;

        boolean SimNao = numero1 == numero2;

        System.out.println("numeroUm e igual a numero2?" + SimNao);

        SimNao = numero1 != numero2;

        System.out.println("numeroUm e diferente a numero2?" + SimNao);

        SimNao = numero1 > numero2;

        System.out.println("numeroUm e maior a numero2?" + SimNao);
    }
}
